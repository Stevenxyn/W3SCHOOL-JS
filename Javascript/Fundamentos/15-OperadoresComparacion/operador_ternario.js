/*Sintaxis
variablename = (condition) ? value1:value2 

Operador condicional (ternario)
JavaScript también contiene un operador condicional que asigna un valor a una variable en función de alguna condición.
*/

let edad = 17;
const seguridad = (edad >= 18) ? "eres mayor" : "eres menor";
console.log(seguridad);

const validadEdad = (edad >= 18) ? true : false 
console.log(validadEdad);